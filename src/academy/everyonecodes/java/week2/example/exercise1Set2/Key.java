package academy.everyonecodes.java.week2.example.exercise1Set2;

public class Key {
    private String name;
    private String keyphrase;

    public Key(String name, String keyphrase) {
        this.name = name;
        this.keyphrase = keyphrase;
    }

    public String getName() {
        return name;
    }

    public String getKeyphrase() {
        return keyphrase;
    }
}
