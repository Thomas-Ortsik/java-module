package academy.everyonecodes.java.week5.set1.exercise1;

public class Person {
    private int age;
    private String name;

    public Person(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }


    public String getName() {
        return name;
    }

}
